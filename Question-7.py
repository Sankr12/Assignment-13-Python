''' Write a python program to print all items by referring to their index number 
(thislist = ["Java", "SQL", "C", "Reactnative", "Javascript", "Python"])''' 

print()
thislist = ["Java", "SQL", "C", "Reactnative", "Javascript", "Python"]
i=0

while i<len(thislist):
    print(thislist[i],end=' ')
    i+=1
    
print()
